const BrazilMap: React.FC<BrazilMapProps> = ({ data, selectedState, onSelectState, heatmapMode = false }) => {
  const [zoom, setZoom] = useState(1);
  const [pan, setPan] = useState({ x: 0, y: 0 });
  const [isDragging, setIsDragging] = useState(false);
  const lastPos = useRef<{ x: number, y: number } | null>(null);
  
  // Reiniciar zoom quando muda a seleção externa
  useEffect(() => {
      setZoom(1);
      setPan({ x: 0, y: 0 });
  }, [selectedState]);

  const handleSelect = (uf: string) => {
    // Previne clique acidental se estiver arrastando
    if (!isDragging && onSelectState) onSelectState(uf);
  };

  const handleZoom = (delta: number) => {
      setZoom(prev => {
          const newZoom = Math.min(Math.max(prev + delta, 1), 4);
          if (newZoom === 1) setPan({ x: 0, y: 0 }); // Reset pan se zoom for 1
          return newZoom;
      });
  };

  const resetMap = () => {
      setZoom(1);
      setPan({ x: 0, y: 0 });
  };

  const onPointerDown = (e: React.PointerEvent) => {
      if (zoom > 1) {
          setIsDragging(false); // Inicia como falso, vira true apenas se mover
          lastPos.current = { x: e.clientX, y: e.clientY };
          (e.currentTarget as Element).setPointerCapture(e.pointerId);
      }
  };

  const onPointerMove = (e: React.PointerEvent) => {
      if (lastPos.current && zoom > 1) {
          const dx = e.clientX - lastPos.current.x;
          const dy = e.clientY - lastPos.current.y;
          
          // Se moveu mais que 5px, considera arrasto
          if (Math.abs(dx) > 2 || Math.abs(dy) > 2) setIsDragging(true);

          setPan(p => ({ x: p.x + dx, y: p.y + dy }));
          lastPos.current = { x: e.clientX, y: e.clientY };
      }
  };

  const onPointerUp = (e: React.PointerEvent) => {
      lastPos.current = null;
      (e.currentTarget as Element).releasePointerCapture(e.pointerId);
      // Timeout curto para limpar flag de dragging após o click event processar
      setTimeout(() => setIsDragging(false), 50);
  };

  const handleKeyDown = (e: React.KeyboardEvent, id: string) => {
      if (e.key === 'Enter' || e.key === ' ') {
          e.preventDefault();
          handleSelect(id);
      }
  };

  // Lógica de Cor para Heatmap
  const { getColor } = useMemo(() => {
      if (!data || !heatmapMode) return { getColor: () => undefined };

      const vals = Object.values(data) as number[];
      const max = Math.max(...vals, 1);

      const getColor = (uf: string) => {
          const value = data[uf] || 0;
          const intensity = value / max;
          // Interpolação simples entre Azul Claro (quase transparente) e Azul Forte
          // Base Color: 53, 92, 245 (RGB do Blue-500 do Tailwind)
          return `rgba(53, 92, 245, ${0.1 + (intensity * 0.9)})`;
      };

      return { getColor };
  }, [data, heatmapMode]);

  return (
    <div className="box-mapa w-full h-full flex items-center justify-center relative select-none overflow-hidden bg-blue-50/30 dark:bg-transparent touch-none">
      <style>{`
        #map {
            width: 100%;
            height: 100%;
            max-width: 600px;
            display: block;
            margin: 0 auto;
            overflow: visible;
            cursor: grab;
        }
        #map:active {
            cursor: grabbing;
        }
        #map .state {
            cursor: pointer;
            transition: fill 0.3s ease, stroke 0.3s ease;
            outline: none;
        }
        #map .state:focus-visible .shape {
            stroke: #355CF5;
            stroke-width: 3px;
            z-index: 20;
        }
        #map .state .shape {
            fill: #e5e7eb; /* Gray-200 default */
            transition: all .3s ease-in-out;
            stroke: #fff;
            stroke-width: 1px;
        }
        
        /* Dark Mode Default */
        .dark #map .state .shape { fill: #374151; stroke: #1f2937; }

        /* Heatmap Overrides */
        ${!heatmapMode ? `
            #map .state:hover .shape,
            #map .state.active .shape {
                fill: #355CF5 !important;
            }
        ` : `
            #map .state:hover .shape {
                filter: brightness(0.8);
                stroke: #355CF5;
                stroke-width: 2px;
                z-index: 10;
            }
        `}

        /* LABELS */
        #map .state .label_icon_state {
            fill: #666;
            font-family: Arial, sans-serif;
            font-size: 11px;
            font-weight: 600;
            pointer-events: none;
            text-shadow: 0 1px 2px rgba(255,255,255,0.8);
        }
        .dark #map .state .label_icon_state { fill: #9ca3af; text-shadow: 0 1px 2px rgba(0,0,0,0.8); }
        
        #map .state:hover .label_icon_state,
        #map .state.active .label_icon_state {
            fill: #111;
            font-weight: 900;
        }
        .dark #map .state:hover .label_icon_state { fill: #FFF; }
      `}</style>
      
      {/* Zoom Controls */}
      <div className="absolute bottom-4 right-4 flex flex-col gap-2 z-20 pointer-events-auto">
          <button onClick={() => handleZoom(0.5)} className="p-2.5 bg-white dark:bg-gray-800 rounded-full shadow-lg border border-gray-200 dark:border-gray-700 text-gray-700 dark:text-gray-200 hover:bg-gray-50 active:scale-95 transition-all" aria-label="Aumentar Zoom">
              <ZoomIn size={20}/>
          </button>
          <button onClick={() => handleZoom(-0.5)} className="p-2.5 bg-white dark:bg-gray-800 rounded-full shadow-lg border border-gray-200 dark:border-gray-700 text-gray-700 dark:text-gray-200 hover:bg-gray-50 active:scale-95 transition-all" aria-label="Diminuir Zoom">
              <ZoomOut size={20}/>
          </button>
          {zoom > 1 && (
              <button onClick={resetMap} className="p-2.5 bg-blue-600 text-white rounded-full shadow-lg hover:bg-blue-700 active:scale-95 transition-all animate-in zoom-in" aria-label="Resetar Mapa">
                  <RotateCcw size={20}/>
              </button>
          )}
      </div>

      {zoom > 1 && (
          <div className="absolute top-4 left-1/2 -translate-x-1/2 bg-black/60 text-white text-[10px] font-bold px-3 py-1 rounded-full pointer-events-none backdrop-blur-sm flex items-center gap-2 z-20">
              <Move size={10} /> Arraste para mover
          </div>
      )}

      <div 
        className="w-full h-full"
        onPointerDown={onPointerDown}
        onPointerMove={onPointerMove}
        onPointerUp={onPointerUp}
        onPointerLeave={onPointerUp}
      >
        <svg 
            id="map" 
            xmlns="http://www.w3.org/2000/svg" 
            viewBox="0 0 460 465" 
            aria-label="Mapa do Brasil Interativo" 
            role="img"
            style={{ 
                transform: `scale(${zoom}) translate(${pan.x / zoom}px, ${pan.y / zoom}px)`, 
                transition: isDragging ? 'none' : 'transform 0.3s ease-out'
            }}
        >
            <g className="model-davi">
                {STATES_DATA.map((state) => {
                    const isActive = selectedState === state.id;
                    const heatColor = heatmapMode ? getColor(state.id) : undefined;
                    
                    return (
                        <g 
                            key={state.id} 
                            className={`state ${isActive ? 'active' : ''}`} 
                            onClick={() => handleSelect(state.id)}
                            onKeyDown={(e) => handleKeyDown(e, state.id)}
                            tabIndex={0}
                            role="button"
                            aria-label={`Selecionar estado ${state.id}`}
                            aria-pressed={isActive}
                        >
                            {state.paths ? (
                                state.paths.map((p, i) => (
                                    <path 
                                        key={i} 
                                        d={p.d} 
                                        className="shape" 
                                        transform={p.transform} 
                                        style={{ ...p.style, fill: heatColor }} 
                                    />
                                ))
                            ) : (
                                <path 
                                    d={state.path!} 
                                    className="shape" 
                                    style={{ ...state.style, fill: heatColor }} 
                                />
                            )}
                            <text x={state.tx} y={state.ty} className="label_icon_state" textAnchor="middle">
                                {state.id}
                            </text>
                        </g>
                    );
                })}
            </g>
        </svg>
      </div>
    </div>
  );
};

export default BrazilMap;
